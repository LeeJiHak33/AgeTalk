package kr.ac.kopo.model;

public class Match {
private int matchid;
private String sid;
private String yid;

public int getMatchid() {
	return matchid;
}
public void setMatchid(int matchid) {
	this.matchid = matchid;
}

public String getSid() {
	return sid;
}
public void setSid(String sid) {
	this.sid = sid;
}
public String getYid() {
	return yid;
}
public void setYid(String yid) {
	this.yid = yid;
}
}

